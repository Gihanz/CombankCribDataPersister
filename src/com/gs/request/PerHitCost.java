package com.gs.request;

public class PerHitCost {
	
	private String hit;
	private String noHit;
	private String multiHit;
	private String error;
	
	/**
	 * @return the hit
	 */
	public String getHit() {
		return hit;
	}
	/**
	 * @param hit the hit to set
	 */
	public void setHit(String hit) {
		this.hit = hit;
	}
	/**
	 * @return the noHit
	 */
	public String getNoHit() {
		return noHit;
	}
	/**
	 * @param noHit the noHit to set
	 */
	public void setNoHit(String noHit) {
		this.noHit = noHit;
	}
	/**
	 * @return the multiHit
	 */
	public String getMultiHit() {
		return multiHit;
	}
	/**
	 * @param multiHit the multiHit to set
	 */
	public void setMultiHit(String multiHit) {
		this.multiHit = multiHit;
	}
	/**
	 * @return the error
	 */
	public String getError() {
		return error;
	}
	/**
	 * @param error the error to set
	 */
	public void setError(String error) {
		this.error = error;
	}
	
}
